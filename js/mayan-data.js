// ความหมายละเอียด 20 Day Signs + 13 Galactic Tones
// Oracle positions: Guide / Analog / Antipode / Occult

// ─── ORACLE CALCULATIONS ───
// Antipode  = ((signIdx + 9) % 20)      — ตรงข้าม
// Analog    = ((signIdx + 5) % 20)      — เสริม
// Occult    = 19 - signIdx              — ซ่อน
// Guide     = ขึ้นกับ Tone group (1-4 → ตัวเอง, 5-8 → +4, 9-12 → +8, 13 → +12) % 20

function getOracleKins(signIdx, toneNum) {
  var antipodeIdx = (signIdx + 9) % 20;
  var analogIdx   = (signIdx + 5) % 20;
  var occultIdx   = 19 - signIdx;
  // Guide: ขึ้นกับ tone group
  var toneGroup   = Math.floor((toneNum - 1) / 4); // 0,1,2,3
  var guideIdx    = (signIdx + toneGroup * 4) % 20;
  return {
    guide:    guideIdx,
    analog:   analogIdx,
    antipode: antipodeIdx,
    occult:   occultIdx
  };
}

